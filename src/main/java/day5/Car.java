package day5;

public class Car {
    private  int    jahr;
    private String farbe;
    private String model;

    public void setJahr(int j) {
        this.jahr = j;
    }
    public void setFarbe(String f) {
        this.farbe = f;
    }
    public void setModel(String m) {
        this.model = m;
    }
    public int getJahr() {
        return jahr;
    }
    public String getFarbe() {
        return farbe;
    }
    public String getModel() {
        return model;
    }
}
